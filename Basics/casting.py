# Integers
a = int(1)   # x will be 1
b = int(2.8) # y will be 2
c = int("3") # z will be 3

# Floats
A = float(1)     # x will be 1.0
B = float(2.8)   # y will be 2.8
C = float("3")   # z will be 3.0
D = float("4.2") # w will be 4.2

# Strings
x = str("s1") # x will be 's1'
y = str(2)    # y will be '2'
z = str(3.0)  # z will be '3.0'

print(a)
print(b)
print(C)
print(A)
print(B)
print(C)
print(D)
